# int
x = 5
print(x)

# float
x = 4.5
print(x)

# String
x = "hello"
print(x)

# List
x = [1, 2, 3, 4, 5]
print(x[4])
print(x)

# Tuple
x = (1, 2, 3, 4, 2)
print(x[3])
print(x)

# Dict
x = {"name": "Dinesh", "Age": 23}
print(x.get("name"))
print(x)

# Set
x = {"Dinesh", 23, "Hello"}
print(x)
