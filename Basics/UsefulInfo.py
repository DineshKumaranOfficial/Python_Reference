# Matrix
x = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
print(x[0][1])

# Ternary Operator
print("True Statement" if 10 < 12 else "False Statement")

# For Loop
x = [1, 2, 3, 4, 5, 6, 7, 8]
for num in x:
    print(num, end='')

# While Loop
a = 2
while a <= 4:
    print(f"a is {a}")
    a += 1
